var myapp = angular.module('myapp',['ui.router']);

myapp.config(['$stateProvider','$urlRouterProvider','$locationProvider',
    function($stateProvider,$urlRouterProvider,$locationProvider){
        $locationProvider.hashPrefix('');
        $locationProvider.html5Mode(true);

        $stateProvider.state('home',{
            url:'/home',
            views:{
                'viewMain':{
                    templateUrl:'/views/home.html',
                    controller:'home.ctrl'
                }
            }
        }).state('contact',{
            url:'/contact',
            views:{
                'viewMain':{
                    templateUrl:'/views/contact.html',
                    controller:'contact.ctrl'
                }
            }
        }).state('about',{
            url:'/about',
            views:{
                'viewMain':{
                    templateUrl:'/views/about.html'
                }
            }
        }).state('construct',{
            url:'/construct',
            views:{
                'viewMain':{
                    templateUrl:'/views/construct.html'
                }
            }
        }).state('person',{
            url:'/person',
            views:{
                'viewMain':{
                    templateUrl:'/views/person.html',
                    controller:'person.ctrl'
                }
            }
        }).state('model',{
            url:'/model',
            views:{
                'viewMain':{
                    templateUrl:'/views/model.html',
                    controller:'model.ctrl'
                }
            }
        }).state('new1',{
            url:'/new1',
            views:{
                'viewMain':{
                    templateUrl:'/views/new1.tpl.html',
                    controller:'new1.tpl.ctrl'
                }
            }
        }).state('new2',{
            url:'/new2',
            views:{
                'viewMain':{
                    templateUrl:'/views/new2.tpl.html',
                    controller:'new2.tpl.ctrl'
                }
            }
        }).state('new3',{
            url:'/new3',
            views:{
                'viewMain':{
                    templateUrl:'/views/new3.tpl.html',
                    controller:'new3.tpl.ctrl'
                }
            }
        }).state('detail',{
            url:'/detail/:id',
            views:{
                'viewMain':{
                    templateUrl:'/views/detail.tpl.html',
                    controller:'detail.tpl.ctrl'
                }
            }
        });

        $urlRouterProvider.otherwise('/home');
}]);
